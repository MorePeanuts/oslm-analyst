"""
Migrate old data from oslm-crawler/data to output/, with the following file mappings:

- merged-models-info.jsonl -> model_data.jsonl
- merged-datasets-info.jsonl -> dataset_data.jsonl
- accumulated-models-info.jsonl -> acc_model_data.jsonl
- accumulated-daatsets-info.jsonl -> acc_dataset_data.jsonl
- model-summary.csv -> model_summary.csv
- data-summary.csv -> dataset_summary.csv
- infra-summary.csv -> infra_summary.csv
- eval-summary.csv -> eval_summary.csv
- model-accumulated-summary.csv -> acc_model_summary.csv
- data-accumulated-summary.csv -> acc_dataset_summary.csv
- model-summary-delta.csv -> delta_model_summary.csv
- data-summary-delta.csv -> delta_dataset_summary.csv
- model-rank.csv -> model_rank.csv
- data-rank.csv -> dataset_rank.csv
- infra-rank.csv -> infra_rank.csv
- eval-rank.csv -> eval_rank.csv
- overall-rank.csv -> overall_rank.csv
- model-accumulated-rank.csv -> acc_model_rank.csv
- data-accumulated-rank.csv -> acc_dataset_rank.csv
- overall-accumulated-rank.csv -> acc_overall_rank.csv
- domestic-rank.csv -> CN_overall_rank.csv
- domestic-accumulated-rank.csv -> CN_acc_overall_rank.csv

New data is generated by code in src/oslm-analyst/processors/osir_lmts.py.

If fields are missing in old data, set them to null in new data.

Old data structure reference: oslm-crawler/data/2026-02-09

New data structure reference: output/osir-lmts_2026-03
"""

import json
import jsonlines
import pandas as pd
from pathlib import Path
from datetime import datetime
from loguru import logger


def convert_old_date_to_month(old_date: str) -> str:
    """Convert old date format (YYYY-MM-DD) to month format (YYYY-MM)"""
    try:
        dt = datetime.strptime(old_date, '%Y-%m-%d')
        return dt.strftime('%Y-%m')
    except ValueError:
        # If already in YYYY-MM format, return as is
        return old_date


def migrate_model_jsonl(old_path: Path, new_path: Path, is_accumulated: bool = False):
    """Migrate model JSONL file"""
    records = []
    with jsonlines.open(old_path) as reader:
        for line in reader:
            # Build identifier
            repo = line.get('repo', line.get('org', ''))
            model_name = line.get('model_name', '')
            if repo and model_name:
                identifier = f'{repo}/{model_name}'
            else:
                identifier = model_name or repo

            date_crawl = line.get('date_crawl', '')
            if not is_accumulated:
                date_crawl = convert_old_date_to_month(date_crawl)

            if is_accumulated:
                # Accumulated data format
                record = {
                    'identifier': identifier,
                    'date_crawl': date_crawl,
                    'likes': line.get('likes') if line.get('likes') is not None else None,
                    'discussions': line.get('community') if line.get('community') is not None else None,
                    'descendants': line.get('descendants') if line.get('descendants') is not None else None,
                    'modality': line.get('modality'),
                    'downloads': line.get('accumulated_downloads') if line.get('accumulated_downloads') is not None else None,
                }
            else:
                # Regular data format
                record = {
                    'identifier': identifier,
                    'date_crawl': date_crawl,
                    'downloads_last_month': line.get('downloads_last_month') if line.get('downloads_last_month') is not None else None,
                    'likes': line.get('likes') if line.get('likes') is not None else None,
                    'discussions': line.get('community') if line.get('community') is not None else None,
                    'descendants': line.get('descendants') if line.get('descendants') is not None else None,
                    'modality': line.get('modality'),
                }
            records.append(record)

    new_path.parent.mkdir(parents=True, exist_ok=True)
    with jsonlines.open(new_path, mode='w') as writer:
        for record in records:
            writer.write(record)

    logger.info(f'Migrated {len(records)} records from {old_path.name} to {new_path.name}')


